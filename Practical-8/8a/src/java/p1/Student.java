package p1;

import javax.persistence.Entity;
import javax.persistence.Id;

/**
 *
 * @author pursh
 */
@Entity
public class Student {
    @Id
    private int bid;
     private String bname;

    public int getBid() {
        return bid;
    }

    public void setBid(int bid) {
        this.bid = bid;
    }

    public String getBname() {
        return bname;
    }

    public void setBname(String bname) {
        this.bname = bname;
    }
   
    
}
